int num1 = 8, num2 = -8;

System.out.println("~ 연산자에 의한 결과 : "+ ~num1);
//~   연산자에 의한 결과 : -9

System.out.println("<< 연산자에 의한 결과 : "+ (num1 << 2));
//<<  연산자에 의한 결과 : 32

System.out.println(">> 연산자에 의한 결과 : "+ (num2 >> 2));
//>>  연산자에 의한 결과 : -2

System.out.println(">>> 연산자에 의한 결과 : "+ (num1 >>> 2));
//>>> 연산자에 의한 결과 : 2

System.out.println(">>> 연산자에 의한 결과 : "+ (num2 >>> 2));
//>>> 연산자에 의한 결과 : 1073741822
