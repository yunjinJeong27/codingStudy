int num1 = 8, num2 = 4;

System.out.println("+ 연산자에 의한 결과 : "+ (num1 + num2));
System.out.println("- 연산자에 의한 결과 : "+ (num1 - num2));

System.out.println("* 연산자에 의한 결과 : "+ (num1 * num2));

System.out.println("/ 연산자에 의한 결과 : "+ (num1 / num2));
System.out.println("% 연산자에 의한 결과 : "+ (num1 % num2));

// + 연산자에 의한 결과 : 12
// - 연산자에 의한 결과 : 4
// * 연산자에 의한 결과 : 32
// / 연산자에 의한 결과 : 2
// % 연산자에 의한 결과 : 0